// Normalise un numéro de compte au format 8 chiffres de la base réelle.
// Un compte SYSCOHADA à 6 chiffres est complété par des zéros à droite ; on
// tolère aussi les espaces et un éventuel séparateur.
export function normaliserCompte(compte) {
  const c = String(compte || "").replace(/[^0-9]/g, "");
  if (!c) return "";
  return c.length < 8 ? c.padEnd(8, "0") : c.slice(0, 8);
}

// Index rapide compte -> intitulé (clés à 8 chiffres).
export const INTITULE_PAR_COMPTE = Object.fromEntries(
  PLAN_COMPTABLE.map((c) => [c.compte, c.intitule])
);

export function intituleCompte(compte) {
  return INTITULE_PAR_COMPTE[normaliserCompte(compte)] || "";
}
