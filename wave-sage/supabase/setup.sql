-- 1) Plan comptable SYSCOHADA révisé (référentiel des comptes généraux) -----
create table if not exists plan_comptable (
  compte text primary key,
  intitule text not null
);

-- 2) Règles d'imputation par mots-clés --------------------------------------
create table if not exists regles_imputation (
  id bigint generated always as identity primary key,
  priorite int not null default 100,
  sens text not null default 'sortie' check (sens in ('sortie','entree','tous')),
  mots_cles text[] not null default '{}',
  compte text not null references plan_comptable(compte),
  libelle text not null default '',
  actif boolean not null default true
);

-- 3) Comptes mémorisés par contrepartie -------------------------------------
create table if not exists mappings_contrepartie (
  contrepartie text primary key,
  compte text not null references plan_comptable(compte)
);

-- 4) Paramètres comptables (clé / valeur) -----------------------------------
create table if not exists parametres (
  cle text primary key,
  valeur text not null
);

-- 5) Historique des imports -------------------------------------------------
create table if not exists imports (
  id bigint generated always as identity primary key,
  nom_fichier text,
  periode_debut date,
  periode_fin date,
  nb_pieces int not null default 0,
  nb_lignes int not null default 0,
  total_debit bigint not null default 0,
  total_credit bigint not null default 0,
  date_import timestamptz not null default now()
);

-- 6) Écritures générées (audit / traçabilité) -------------------------------
create table if not exists ecritures (
  id bigint generated always as identity primary key,
  import_id bigint references imports(id) on delete cascade,
  reference text not null default '',
  journal text not null,
  piece_date date,
  compte text not null,
  libelle text not null default '',
  debit bigint not null default 0 check (debit >= 0),
  credit bigint not null default 0 check (credit >= 0)
);
create index if not exists ecritures_import_idx on ecritures (import_id);

-- Sécurité (RLS) : MVP, clé publique autorisée en lecture/écriture ----------
alter table plan_comptable        enable row level security;
alter table regles_imputation     enable row level security;
alter table mappings_contrepartie enable row level security;
alter table parametres            enable row level security;
alter table imports               enable row level security;
alter table ecritures             enable row level security;
drop policy if exists anon_all on plan_comptable;
create policy anon_all on plan_comptable for all to anon, authenticated using (true) with check (true);
drop policy if exists anon_all on regles_imputation;
create policy anon_all on regles_imputation for all to anon, authenticated using (true) with check (true);
drop policy if exists anon_all on mappings_contrepartie;
create policy anon_all on mappings_contrepartie for all to anon, authenticated using (true) with check (true);
drop policy if exists anon_all on parametres;
create policy anon_all on parametres for all to anon, authenticated using (true) with check (true);
drop policy if exists anon_all on imports;
create policy anon_all on imports for all to anon, authenticated using (true) with check (true);
drop policy if exists anon_all on ecritures;
create policy anon_all on ecritures for all to anon, authenticated using (true) with check (true);

